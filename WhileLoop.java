
public class WhileLoop {

	public static void main(String[] args) {
		
		// while loop
		int i = 1;
		while(i<=3) {
			System.out.println(i);
			i++;
		}
		
		// 		Quick quize Question
		System.out.println("Natural number from 100 to 200.");
		int a = 100;
		while(a<=200) {
			System.out.print(a+"\t");
			a++;
		}
		
		
	}

}
